const fillTable = (arr) =>
{
	let table = document.querySelector('.table');
	let tableBody = table.tBodies[0];
	tableBody.innerHTML = '';
	for(let i = 0; i < arr.length; i++)
	{
		let row = tableBody.insertRow(i);
		for(let j = 0; j < arr[i].length; j++)
		{
			let cell = row.insertCell(j);
			cell.innerHTML = arr[i][j];
		}
	}
};

